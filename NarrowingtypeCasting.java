public class NarrowingtypeCasting {
    public static void main(String args[]){
        // double myNum=10.0d;
        float myNum=10.0f;
        int myInt=(int) myNum;       //Manual Casting
        System.out.println(myNum);   //output 10.0
        System.out.println(myInt);   //output 10

    }
    
}
